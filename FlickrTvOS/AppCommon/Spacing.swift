import Foundation

/// Enum representing common spacing values.
enum Spacing {
    /// A tiny spacing value (2.0 points).
    static let tiny: CGFloat = 2.0
    /// An extra small spacing value (4.0 points).
    static let extraSmall: CGFloat = 4.0
    /// A small spacing value (8.0 points).
    static let small: CGFloat = 8.0
    /// A medium spacing value (12.0 points).
    static let medium: CGFloat = 12.0
    /// A regular spacing value (16.0 points).
    static let regular: CGFloat = 16.0
    /// A large spacing value (24.0 points).
    static let large: CGFloat = 24.0
    /// An extra large spacing value (32.0 points).
    static let extraLarge: CGFloat = 32.0
    /// A huge spacing value (48.0 points).
    static let huge: CGFloat = 48.0
    /// An enormous spacing value (64.0 points).
    static let enormous: CGFloat = 64.0
}
